// ── Feature definitions ───────────────────────────────────────────────────────

const FEATURES = [
  { key: 'deny_netlink',           label: 'Block AF_NETLINK',          desc: 'iptables, nftables, ip route, auditctl',           def: true  },
  { key: 'deny_mount',             label: 'Block mount',               desc: 'Prevent bind-mounting over protected paths',        def: true  },
  { key: 'deny_ptrace',            label: 'Block ptrace',              desc: 'Prevent code injection into root daemons',          def: true  },
  { key: 'deny_cap_audit_control', label: 'Block CAP_AUDIT_CONTROL',   desc: 'Defence-in-depth for auditctl',                     def: true  },
  { key: 'deny_cap_net_admin',     label: 'Block CAP_NET_ADMIN',       desc: 'Raw sockets, network admin (may block iproute2)',   def: false },
  { key: 'deny_cap_sys_module',    label: 'Block CAP_SYS_MODULE',      desc: 'Prevent kernel module loading',                     def: true  },
  { key: 'deny_cap_mac_admin',     label: 'Block CAP_MAC_ADMIN',       desc: 'Prevent SELinux policy changes',                    def: true  },
  { key: 'deny_cap_sys_rawio',     label: 'Block CAP_SYS_RAWIO',       desc: '/dev/mem, /dev/port raw I/O access',                def: true  },
  { key: 'deny_cap_sys_boot',      label: 'Block CAP_SYS_BOOT',        desc: 'Prevent kexec_load / reboot from session',          def: true  },
  { key: 'deny_systemd_ipc',       label: 'Restrict Systemd D-Bus IPC', desc: 'Blocks systemctl, loginctl (off by default)',      def: false },
] as const

type FeatureKey = typeof FEATURES[number]['key']
type ProtectKey = 'files' | 'forbidden' | 'noexec' | 'devices' | 'proc' | 'sockets' | 'processes'

interface SandboxState {
  enabled: boolean
  features: Record<FeatureKey, boolean>
  files: string[]
  forbidden: string[]
  noexec: string[]
  devices: string[]
  proc: string[]
  sockets: string[]
  processes: string[]
}

// ── YAML helpers ──────────────────────────────────────────────────────────────

function defaultState(): SandboxState {
  return {
    enabled: true,
    features: Object.fromEntries(FEATURES.map(f => [f.key, f.def])) as Record<FeatureKey, boolean>,
    files: [], forbidden: [], noexec: [], devices: [], proc: [], sockets: [], processes: [],
  }
}

function parseYaml(yaml: string): SandboxState {
  const s = defaultState()
  if (!yaml) return s
  let section: string | null = null
  let currentKey: string | null = null
  for (const rawLine of yaml.split('\n')) {
    const line = rawLine.replace(/#.*/, '').trimEnd()
    if (!line.trim()) continue
    const em = line.match(/^enabled:\s*(true|false)/)
    if (em) { s.enabled = em[1] === 'true'; continue }
    if (/^features:/.test(line)) { section = 'features'; currentKey = null; continue }
    if (/^protect:/.test(line))  { section = 'protect';  currentKey = null; continue }
    if (section === 'features') {
      const m = line.match(/^ {2}(deny_\w+):\s*(true|false)/)
      if (m) (s.features as any)[m[1]] = m[2] === 'true'
    } else if (section === 'protect') {
      const km = line.match(/^ {2}(files|forbidden|noexec|devices|proc|sockets|processes):/)
      if (km) { currentKey = km[1]; continue }
      const im = line.match(/^ {4}- (.+)/)
      if (im && currentKey) {
        let v = im[1].trim()
        if ((v.startsWith('"') && v.endsWith('"')) || (v.startsWith("'") && v.endsWith("'"))) v = v.slice(1, -1)
        ;(s as any)[currentKey].push(v)
      }
    }
  }
  return s
}

function buildYaml(s: SandboxState): string {
  let yaml = '# /etc/sudo-logger/sandbox.yaml\n# Generated by replay-server UI\n\n'
  if (!s.enabled) yaml += 'enabled: false\n\n'
  yaml += 'features:\n'
  for (const f of FEATURES) yaml += `  ${f.key}: ${s.features[f.key]}\n`
  yaml += '\nprotect:\n'
  const keys: ProtectKey[] = ['files', 'forbidden', 'noexec', 'devices', 'proc', 'sockets', 'processes']
  for (const k of keys) {
    const list = s[k]
    if (list.length) { yaml += `  ${k}:\n`; list.forEach(v => { yaml += `    - ${v}\n` }) }
  }
  return yaml
}

